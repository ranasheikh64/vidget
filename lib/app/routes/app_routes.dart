abstract class Routes {
  Routes._();
  static const MAIN = _Paths.MAIN;
  static const HOME = _Paths.HOME;
  static const DOWNLOADS = _Paths.DOWNLOADS;
  static const BROWSER = _Paths.BROWSER;
  static const FILES = _Paths.FILES;
  static const SETTINGS = _Paths.SETTINGS;
  static const VAULT = _Paths.VAULT;
  static const VIDEO_PLAYER = _Paths.VIDEO_PLAYER;
}

abstract class _Paths {
  _Paths._();
  static const MAIN = '/main';
  static const HOME = '/home';
  static const DOWNLOADS = '/downloads';
  static const BROWSER = '/browser';
  static const FILES = '/files';
  static const SETTINGS = '/settings';
  static const VAULT = '/vault';
  static const VIDEO_PLAYER = '/video-player';
}
